    private void StudentinfoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_StudentinfoMouseClicked
        try {
            int row = Studentinfo.getSelectedRow();
            String tableClick = (Studentinfo.getModel().getValueAt(row, 1).toString());
            String sql = "Select 8 from Student_info where Id =' " + tableClick + "'";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();

            if (rs.next()) {
                sname.setText(rs.getString("Name"));
                sid.setText(rs.getString("Id"));
                sun.setText(rs.getString("User_Name"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }//GEN-LAST:event_StudentinfoMouseClicked

